package gameconfig

type ReportPhase struct {
	Title string `json:"title"`
	Body  string `json:"body"`
}

type ReportTemplate struct {
	Title  string
	Body   string
	Phases []ReportPhase
}

func MissionReportTemplate(missionKey string, result string) ReportTemplate {
	if byResult, ok := reportTemplates[missionKey]; ok {
		if template, ok := byResult[result]; ok {
			return template
		}
	}

	return ReportTemplate{
		Title: "Донесение из похода",
		Body:  "Отряд вернулся с короткой вестью. Писарь занёс добычу и потери в княжью книгу.",
		Phases: []ReportPhase{
			{Title: "Возвращение", Body: "Люди прошли ворота молча, неся на плечах мокрые щиты и тугие мешки."},
		},
	}
}

